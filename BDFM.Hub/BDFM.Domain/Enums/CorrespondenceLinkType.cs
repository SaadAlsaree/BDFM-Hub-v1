namespace BDFM.Domain.Enums
{
    public enum CorrespondenceLinkType
    {
        RefersTo = 1,       // الكتاب الحالية تشير إلى الكتاب المرتبطة (الأكثر شيوعًا)
        ReplyTo = 2,        // الكتاب الحالية هي رد على الكتاب المرتبطة
        FollowUpTo = 3,     // الكتاب الحالية هي متابعة للمراسلة المرتبطة
        RelatedTo = 4,      // مجرد ارتباط عام بالموضوع
        Supersedes = 5,     // الكتاب الحالية تلغي/تستبدل الكتاب المرتبطة
        ContinuationOf = 6  // الكتاب الحالية هي استكمال للمراسلة المرتبطة
                            // يمكنك إضافة أنواع أخرى حسب الحاجة
    }
}
